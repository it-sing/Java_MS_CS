package org.example.model;

public class DataY3 {
    private String semester;
    private String stdYear;
    private String stdCode;
    private String stdName;
    private String stdSex;
    private Double dotNet;
    private Double java;
    private Double network;
    private Double analysisDesign;
    private Double statistics;

    public String getStdYear() {
        return stdYear;
    }

    public void setStdYear(String stdYear) {
        this.stdYear = stdYear;
    }

    public String getSemester() {
        return semester;
    }
    public void setSemester(String semester) {
        this.semester = semester;
    }
    // Getter and Setter for stdCode
    public String getStdCode() {
        return stdCode;
    }

    public void setStdCode(String stdCode) {
        this.stdCode = stdCode;
    }

    // Getter and Setter for stdName
    public String getStdName() {
        return stdName;
    }

    public void setStdName(String stdName) {
        this.stdName = stdName;
    }

    // Getter and Setter for stdSex
    public String getStdSex() {
        return stdSex;
    }

    public void setStdSex(String stdSex) {
        this.stdSex = stdSex;
    }

    // Getter and Setter for dotNet
    public Double getDotNet() {
        return dotNet;
    }

    public void setDotNet(Double dotNet) {
        this.dotNet = dotNet;
    }

    // Getter and Setter for java
    public Double getJava() {
        return java;
    }

    public void setJava(Double java) {
        this.java = java;
    }

    // Getter and Setter for network
    public Double getNetwork() {
        return network;
    }

    public void setNetwork(Double network) {
        this.network = network;
    }

    // Getter and Setter for analysisDesign
    public Double getAnalysisDesign() {
        return analysisDesign;
    }

    public void setAnalysisDesign(Double analysisDesign) {
        this.analysisDesign = analysisDesign;
    }

    // Getter and Setter for statistics
    public Double getStatistics() {
        return statistics;
    }

    public void setStatistics(Double statistics) {
        this.statistics = statistics;
    }
}
